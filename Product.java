abstract class Product{
 protected String name;
 protected double price;
 protected double cost;
 
 public Product extend(String name,double price, double cost){
 this.name=name;
 this.price=price;
 this.cost=cost;
 }
 public String name(){
 return name;}
 @override
 public  String toString (){
 return name;
 return cost;
 return price;
  }
 
  } 
 
